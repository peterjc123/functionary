def get_model_param_count(model, trainable_only=False):
    if trainable_only:
        params = model.trainable_parameters()
    else:
        params = model.parameters()

    return sum(param.size for _, param in tree_flatten(params))

def loss_func(model, input_ids, labels, attention_mask=None):
    logits, _ = model(input_ids, cache=attention_mask)
    logits = logits.astype(mx.float32)

    shift_logits = logits[..., :-1, :]
    shift_labels = labels[..., 1:]

    loss_mask = shift_labels != -100
    ce = nn.losses.cross_entropy(shift_logits, shift_labels) * loss_mask
    ntoks = loss_mask.sum()
    ce = ce.sum() / ntoks

    return (ce, logits)


class MLXTrainer(Trainer):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.accumulated_grads = tree_map(
            lambda x: mx.zeros_like(x), self.model.trainable_parameters()
        )

    def _move_model_to_device(self, model, device):
        pass

    def _set_signature_columns_if_needed(self):
        self._signature_columns = ["input_ids", "labels", "label_ids"]

    def _prepare_inputs(self, inputs):
        return super()._prepare_inputs(inputs)

    def _prepare_input(self, data):
        """
        Prepares one `data` before feeding it to the model, be it a tensor or a nested list/dictionary of tensors.
        """
        if isinstance(data, Mapping):
            return type(data)({k: self._prepare_input(v) for k, v in data.items()})
        elif isinstance(data, (tuple, list)):
            return type(data)(self._prepare_input(v) for v in data)
        elif isinstance(data, torch.Tensor):
            return mx.array(data.numpy())
        return data

    def _save(self, output_dir = None, state_dict=None):
        output_dir = output_dir if output_dir is not None else self.args.output_dir
        os.makedirs(output_dir, exist_ok=True)
        logger.info(f"Saving model checkpoint to {output_dir}")

        flat_params = tree_flatten(self.model.trainable_parameters())
        mx.savez(os.path.join(output_dir, "mlx_model.npz"), **dict(flat_params))

        # Good practice: save your training arguments together with the trained model
        torch.save(self.args, os.path.join(output_dir, TRAINING_ARGS_NAME))

    def _save_optimizer_and_scheduler(self, output_dir):
        flat_params = tree_flatten(self.optimizer.state)
        mx.savez(os.path.join(output_dir, "optimizer.npz"), **dict(flat_params))

    def training_step(self, model, inputs):
        inputs = self._prepare_inputs(inputs)
        inputs["is_training"] = True

        loss = self.compute_loss(model, inputs)

        return loss / self.args.gradient_accumulation_steps

    def prediction_step(
        self,
        model,
        inputs,
        prediction_loss_only,
        ignore_keys,
    ):
        has_labels = False if len(self.label_names) == 0 else all(inputs.get(k) is not None for k in self.label_names)
        # For CLIP-like models capable of returning loss values.
        # If `return_loss` is not specified or being `None` in `inputs`, we check if the default value of `return_loss`
        # is `True` in `model.forward`.
        return_loss = inputs.get("return_loss", None)
        if return_loss is None:
            return_loss = self.can_return_loss
        loss_without_labels = True if len(self.label_names) == 0 and return_loss else False

        inputs = self._prepare_inputs(inputs)
        if ignore_keys is None:
            if hasattr(self.model, "config"):
                ignore_keys = getattr(self.model.config, "keys_to_ignore_at_inference", [])
            else:
                ignore_keys = []

        # labels may be popped when computing the loss (label smoothing for instance) so we grab them first.
        if has_labels or loss_without_labels:
            labels = tuple(inputs.get(name) for name in self.label_names)
            if len(labels) == 1:
                labels = labels[0]
        else:
            labels = None

        if has_labels or loss_without_labels:
            loss, outputs = self.compute_loss(model, inputs, return_outputs=True)
            loss = loss.mean()

            if isinstance(outputs, dict):
                logits = tuple(v for k, v in outputs.items() if k not in ignore_keys + ["loss"])
            else:
                logits = outputs[1:]
        else:
            loss = None
            outputs = model(**inputs)
            if isinstance(outputs, dict):
                logits = tuple(v for k, v in outputs.items() if k not in ignore_keys)
            else:
                logits = outputs
            # TODO: this needs to be fixed and made cleaner later.
            if self.args.past_index >= 0:
                self._past = outputs[self.args.past_index - 1]

        if prediction_loss_only:
            return (loss, None, None)

        if len(logits) == 1:
            logits = logits[0]

        return (loss, logits, labels)

    def compute_loss(self, model, inputs, return_outputs=False):
        if type(self.data_collator).__name__ == 'SimpleCollator':
            inputs.pop("attention_mask", None)
        if inputs.pop("is_training", False):
            loss_value_and_grad = nn.value_and_grad(model, loss_func)

            result, grad = loss_value_and_grad(model, **inputs)
            self.accumulated_grads = tree_map(
                lambda acc, new: acc + new * (1.0 / self.args.gradient_accumulation_steps),
                self.accumulated_grads,
                grad,
            )

            tree_map(
                lambda grad: mx.eval(grad),
                self.accumulated_grads,
            )
        else:
            result = loss_func(model, **inputs)

        if return_outputs:
            return result
        else:
            return result[0]

